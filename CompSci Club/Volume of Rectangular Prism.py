# This program calculates the volume of a rectangular prism
# when given the height, length, and width

width = int(input("Input the width: "))  # Declares the variable width
height = int(input("Input the height: "))  # Declares the variable height
length = int(input("Input the length: "))  # Declares the variable length

volume = width*height*length  # Declares the variable volume as width times height times length

print("The volume is", volume, "m^3")  # Prints what the volume of the room is
