#  This program calculates the perimiter of a room

width = int(input("Input the width: "))  # Declares the variable width
length = int(input("Input the length: "))  # Declares the variable length


perimiter = 2*width + 2*length  # Declares the variable perimiter

print("The perimiter of a room is", perimiter, "feet.")  # Prints what the perimiter of the room is
