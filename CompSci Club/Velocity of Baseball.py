# This program calculates the velocity of a ball when given
# the distance and time that it travels for

distance = input("Distance that the ball travels:")  # Declares the variable distance
time = input("How long was the ball moving for:")  # Declares the variable time

velocity = distance/time  # Declares the variable velocity

print("The velocity of the ball is", velocity, "m/s.")  # Prints the velocity of the ball
